/* Pergunte ao usuário qual é o dia da semana. Se a 
resposta for "Sábado" ou "Domingo", mostre "Bom fim de 
semana!". Caso contrário, mostre "Boa semana!"*/

let diaDaSemana = prompt('Qual é o dia da semana?');
let finalDeSemana1 = 'Sabádo';
let finalDeSemana2 = 'Domingo';

if (diaDaSemana == finalDeSemana1){
    alert('Bom fim de semana!');
}else if (diaDaSemana == finalDeSemana2){
    alert('Bom fim de semana!');
}else{
    alert('Boa semana');
}